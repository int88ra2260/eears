export const getTranslation = (lang, path) => {
  const keys = path.split('.');
  let value = translations[lang] || translations.zh;
  for (const key of keys) {
    value = value?.[key];
    if (value === undefined) return path;
  }
  return typeof value === 'string' ? value : path;
};
